"""Модуль для подсчета веса Хэмминга (количества единичных битов в числе)."""


def weight_haminga(n):
    """Подсчитывает количество единичных битов в двоичном представлении числа.

    Args:
        n: положительное целое число

    Returns:
        int: количество установленных битов (вес Хэмминга)
    """

    return bin(n).count("1")


print(weight_haminga(11))
print(weight_haminga(128))
print(weight_haminga(2147483645))
